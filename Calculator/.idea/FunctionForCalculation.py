import math


#//=========================================//
#// this function performs only addition   //
#//=======================================//

def Addition(a, b):
    number_one = a
    number_two = b
    total = number_one + number_two
    return total

#//=========================================//
#// this function is for subtraction only  //
#//=======================================//

def Subtraction(a, b):
    number_one = a
    number_two = b
    total = number_one - number_two
    return total


#//==========================================//
#// this function is for multiplication only//
#//========================================//

def Multiplication(a, b):
    number_one = a
    number_two = b
    total = number_one * number_two
    return total

#//=======================================//
#// this function is for division only   //
#//=====================================//

def Division(a, b):
    number_one = a
    number_two = b
    total = number_one / number_two
    return total

#//=========================================//
#// this function is for squre root only   //
#//=======================================//

def SqureRoot(a):
    squre_root = math.sqrt(a)
    total = squre_root
    return total
